export type BookingLocale = "fr" | "nl" | "en" | "de";
export const BOOKING_LOCALES: BookingLocale[] = ["fr", "nl", "en", "de"];
export const BOOKING_LOCALE_LABELS: Record<BookingLocale, string> = {
  fr: "Français",
  nl: "Nederlands",
  en: "English",
  de: "Deutsch",
};

export function normalizeLocale(v: string | null | undefined): BookingLocale {
  const s = (v ?? "").toLowerCase().slice(0, 2);
  return (BOOKING_LOCALES as string[]).includes(s) ? (s as BookingLocale) : "fr";
}

/** Date longue localisée ("lundi 6 juin 2026" / "maandag 6 juni 2026"...). */
export function localeDate(ymd: string, locale: BookingLocale): string {
  const d = new Date(`${ymd}T00:00:00Z`);
  const tag = { fr: "fr-BE", nl: "nl-BE", en: "en-GB", de: "de-DE" }[locale];
  return new Intl.DateTimeFormat(tag, { weekday: "long", day: "numeric", month: "long", year: "numeric", timeZone: "UTC" }).format(d);
}

/** Remplace {clé} par les valeurs fournies. */
export function fillTemplate(tpl: string, vars: Record<string, string | number> = {}): string {
  return tpl.replace(/\{(\w+)\}/g, (_, k) => (vars[k] != null ? String(vars[k]) : `{${k}}`));
}

