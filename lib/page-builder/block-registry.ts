export interface BlockRegistryEntry<T extends BlockType = BlockType> {
  type: T
  name: string
  description: string
  category: BlockCategory
  icon: string // lucide icon name
  shortcuts?: string[] // slash command aliases, e.g. ['h1','title']
  defaultProps: BlockPropsMap[T]
  variants?: Array<{
    id: string
    name: string
    description?: string
  }>
  /** Whether this block can have children (columns, section, container) */
  canHaveChildren?: boolean
}

type Registry = { [K in BlockType]: BlockRegistryEntry<K> }

/** Union of every registry entry — preserves the discriminated `type` field. */
export type AnyBlockRegistryEntry = Registry[BlockType]

