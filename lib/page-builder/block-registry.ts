// Utility: pre-generate sample heatmap data for the past year
function generateSampleHeatmap() {
  const out: { date: string; value: number }[] = []
  const today = new Date()
  for (let i = 365; i >= 0; i--) {
    const d = new Date(today)
    d.setDate(d.getDate() - i)
    out.push({ date: d.toISOString().slice(0, 10), value: Math.floor(Math.random() * 5) })
  }
  return out
}

export function makeBlock<T extends BlockType>(type: T): Block<T> {
  const entry = BLOCK_REGISTRY[type]
  return {
    id: nanoid(),
    type,
    props: structuredClone(entry.defaultProps) as BlockPropsMap[T],
  } as Block<T>
}

